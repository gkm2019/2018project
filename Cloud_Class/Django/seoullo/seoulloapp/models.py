from django.db import models

class date1(models.Model):
    region = models.CharField(max_length=50)
    visitor_id = models.CharField(max_length=50)
    first_time_seen = models.CharField(max_length=50)
    last_time_seen = models.CharField(max_length=50)

    class Meta:
        verbose_name = "date1"
        verbose_name_plural = "date1"

    def __unicode__(self):
        return '%s %s %s %s' % (self.region, self.visitor_id, self.first_time_seen, self.last_time_seen)

